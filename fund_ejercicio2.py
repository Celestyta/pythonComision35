# Crear una lista con 5 elementos
mi_lista = ["Hola", 2, "Celes", 4, "5"]

# Imprimir el último elemento
print("El último elemento de la lista es:", mi_lista[-1])

# Modificar el valor del tercer elemento
mi_lista[2] = 10

# Agregar dos elementos
mi_lista.append("chau")
mi_lista.append(7)

# Eliminar algún elemento
elemento_a_eliminar = 4
mi_lista.remove(elemento_a_eliminar)

# Imprimir la lista resultante
print("Lista modificada:", mi_lista)